// Minimal createIcons — replaces <i data-lucide="icon-name"> with SVGs
function createIcons({ icons = {} } = {}) {
  const toKebab = name => name.replace(/([A-Z]|\d+)/g, (_, c, i) => (i ? '-' : '') + c.toLowerCase());
  const nameMap = {};
  for (const [k, v] of Object.entries(icons)) nameMap[toKebab(k)] = v;

  document.querySelectorAll('[data-lucide]').forEach(el => {
    const iconNode = nameMap[el.getAttribute('data-lucide')];
    if (!iconNode) return;
    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    svg.setAttribute('width', '18');
    svg.setAttribute('height', '18');
    svg.setAttribute('viewBox', '0 0 24 24');
    svg.setAttribute('fill', 'none');
    svg.setAttribute('stroke', 'currentColor');
    svg.setAttribute('stroke-width', '2');
    svg.setAttribute('stroke-linecap', 'round');
    svg.setAttribute('stroke-linejoin', 'round');
    svg.setAttribute('aria-hidden', 'true');
    svg.style.display = 'block';
    for (const [tag, attrs] of iconNode) {
      const child = document.createElementNS('http://www.w3.org/2000/svg', tag);
      for (const [k, v] of Object.entries(attrs)) child.setAttribute(k, v);
      svg.appendChild(child);
    }
    el.replaceWith(svg);
  });
}

